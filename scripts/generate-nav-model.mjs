/**
 * generate-nav-model — compiles the docs-site navigation into ONE derived model.
 *
 * Phase 1 of ADR-017 (single-source, derived docs nav). This is the generator +
 * artifact only; no UI consumes `nav-model.json` yet (that is a later phase).
 *
 * Source of truth (transitional): `src/app/data/hds-registry.json` entries that
 * opt into the sidebar via `navSection` + `navOrder`, plus the internal routes
 * not tracked in the registry. The output mirrors `buildNavSections()` in
 * `hds-nav-data.ts` exactly (a behavior-preservation test asserts this), but as
 * a serializable artifact: the `getExact`/`getIndent` predicates are flattened
 * to per-item `exact`/`indent` booleans so the model is plain data an Astro
 * `getStaticPaths()` (or any non-React renderer) can consume.
 *
 * A later phase will switch the SOURCE from the registry to each page's
 * colocated `meta` export; the OUTPUT contract (this model) stays the same.
 *
 * Run directly to (re)write `src/app/data/nav-model.json`:
 *   node scripts/generate-nav-model.mjs        # pnpm nav:generate
 *
 * `buildNavModel(repoRoot)` is exported for the drift test. Honors FIXTURE_DIR
 * so it can run against a synthetic mini-root.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DEFAULT_ROOT = path.resolve(__dirname, '..');

const GENERATED_NOTE = 'GENERATED by scripts/generate-nav-model.mjs — do not edit by hand';

// Routes that belong in the sidebar but are not tracked in hds-registry.json.
// (Mirrors INTERNAL_NAV_ITEMS in hds-nav-data.ts.)
const INTERNAL_NAV_ITEMS = [{ path: '/brand-theming', label: 'Brand Theming' }];

/**
 * Map a registry path to its standalone route. The registry carries the
 * monorepo's /hds/* prefix; the standalone site serves every doc page at root.
 * (Mirrors toAppPath in hds-nav-data.ts.)
 */
export function toAppPath(registryPath) {
  if (registryPath === '/hds') return '/';
  return registryPath.startsWith('/hds/') ? registryPath.slice(4) : registryPath;
}

/**
 * Build the serializable nav model from a repo root. Pure read — no writes.
 * @param {string} repoRoot
 * @returns {{ $generated: string, sections: Array<{label:string, items:Array<{path:string,label:string,exact?:boolean,indent?:boolean}>}> }}
 */
export function buildNavModel(repoRoot) {
  const registryPath = path.join(repoRoot, 'src', 'app', 'data', 'hds-registry.json');
  const registry = JSON.parse(readFileSync(registryPath, 'utf8'));

  // Entries that opt into the nav, sorted by navOrder (stable for ties).
  const navEntries = registry
    .filter((e) => e.navSection != null)
    .sort((a, b) => (a.navOrder ?? 999) - (b.navOrder ?? 999));

  // Group by section, preserving first-seen order and de-duping paths
  // (e.g. the two /hds/shape entries collapse to one).
  const sectionMap = new Map();
  const seenPaths = new Set();

  for (const entry of navEntries) {
    const section = entry.navSection;
    if (!sectionMap.has(section)) sectionMap.set(section, []);
    if (!seenPaths.has(entry.path)) {
      seenPaths.add(entry.path);
      sectionMap.get(section).push({ path: toAppPath(entry.path), label: entry.page });
    }
  }

  const sections = [];
  for (const [label, items] of sectionMap) {
    const decorated = items.map((item) => {
      // Flatten the predicates from hds-nav-data.ts to serializable flags.
      if (label === 'Foundations' && item.path === '/color') return { ...item, exact: true };
      if (label === 'Components' && item.path.startsWith('/components')) {
        return { ...item, indent: true };
      }
      return item;
    });
    sections.push({ label, items: decorated });
  }

  // Internal section (routes not tracked in the registry).
  sections.push({ label: 'Internal', items: INTERNAL_NAV_ITEMS.map((i) => ({ ...i })) });

  return { $generated: GENERATED_NOTE, sections };
}

function main() {
  const repoRoot = process.env.FIXTURE_DIR || DEFAULT_ROOT;
  const model = buildNavModel(repoRoot);
  const outPath = path.join(repoRoot, 'src', 'app', 'data', 'nav-model.json');
  writeFileSync(outPath, `${JSON.stringify(model, null, 2)}\n`);
  const count = model.sections.reduce((n, s) => n + s.items.length, 0);
  console.log(
    `OK ${path.relative(repoRoot, outPath)} (${model.sections.length} sections, ${count} items)`,
  );
}

// Run main only when invoked directly (not when imported by the drift test).
if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}
