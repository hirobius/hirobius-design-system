/**
 * generate-nav-model — compiles the docs-site navigation into ONE derived model.
 *
 * ADR-017, Phase 2: the SOURCE OF TRUTH is now each docs page's colocated
 * `export const meta` ({ path, title, section, order, status? }). This generator
 * walks `src/app/pages/hds/`, reads every `meta`, and assembles
 * `src/app/data/nav-model.json`. Adding/moving a page in the nav is now a
 * single-file edit (the page's own `meta`) followed by `pnpm nav:generate`.
 *
 * Output contract is unchanged from Phase 1 (a behavior-preservation test
 * asserts the model still equals what the live `HDS_NAV_SECTIONS` renders): a
 * serializable model where the `getExact`/`getIndent` predicates are flattened
 * to per-item `exact`/`indent` booleans, so it is plain data an Astro
 * `getStaticPaths()` (or any non-React renderer) can consume.
 *
 * Ordering is deterministic: entries sort by `order` then `path`; sections
 * appear in first-seen order of that sorted list (so an Internal page with a
 * high `order` lands last). Duplicate paths collapse to the first occurrence.
 *
 * Run directly to (re)write the artifact:
 *   node scripts/generate-nav-model.mjs        # pnpm nav:generate
 *
 * `buildNavModel(repoRoot)` is exported for the drift test.
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DEFAULT_ROOT = path.resolve(__dirname, '..');

const GENERATED_NOTE = 'GENERATED by scripts/generate-nav-model.mjs — do not edit by hand';
const PAGES_DIR_SEGMENTS = ['src', 'app', 'pages', 'hds'];

/** Recursively collect page source files (excluding tests/stories). */
function collectPageFiles(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = path.join(dir, entry);
    if (statSync(full).isDirectory()) {
      out.push(...collectPageFiles(full));
    } else if (/\.tsx$/.test(entry) && !/\.(test|spec|stories)\.tsx$/.test(entry)) {
      out.push(full);
    }
  }
  return out;
}

/**
 * Extract a page's `export const meta = { … }` as a plain object. `meta` is a
 * flat literal (string/number values, no nested braces), so a targeted match is
 * robust. Returns null when the file has no `meta` or it is incomplete.
 */
export function parseMeta(source) {
  const block = source.match(/export\s+const\s+meta\b[^{]*\{([^}]*)\}/);
  if (!block) return null;
  const body = block[1];

  const readString = (key) => {
    const m = body.match(new RegExp(`\\b${key}\\s*:\\s*(?:'([^']*)'|"([^"]*)")`));
    return m ? (m[1] ?? m[2]) : undefined;
  };
  const readNumber = (key) => {
    const m = body.match(new RegExp(`\\b${key}\\s*:\\s*(-?\\d+)`));
    return m ? Number(m[1]) : undefined;
  };

  const meta = {
    path: readString('path'),
    title: readString('title'),
    section: readString('section'),
    order: readNumber('order'),
    description: readString('description'),
    status: readString('status'),
  };
  if (!meta.path || !meta.title || !meta.section || meta.order == null) return null;
  return meta;
}

/**
 * Build the serializable nav model from a repo root by reading colocated page
 * `meta`. Pure read — no writes.
 * @param {string} repoRoot
 * @returns {{ $generated: string, sections: Array<{label:string, items:Array<{path:string,label:string,exact?:boolean,indent?:boolean}>}> }}
 */
export function buildNavModel(repoRoot) {
  const pagesDir = path.join(repoRoot, ...PAGES_DIR_SEGMENTS);
  const entries = [];
  for (const file of collectPageFiles(pagesDir)) {
    const meta = parseMeta(readFileSync(file, 'utf8'));
    if (meta) entries.push(meta);
  }

  // Deterministic: by order, then path. Section order = first-seen in this list.
  entries.sort((a, b) => a.order - b.order || a.path.localeCompare(b.path));

  const sectionMap = new Map();
  const seenPaths = new Set();
  for (const entry of entries) {
    if (!sectionMap.has(entry.section)) sectionMap.set(entry.section, []);
    if (!seenPaths.has(entry.path)) {
      seenPaths.add(entry.path);
      const item = { path: entry.path, label: entry.title };
      if (entry.description) item.description = entry.description;
      sectionMap.get(entry.section).push(item);
    }
  }

  const sections = [];
  for (const [label, items] of sectionMap) {
    const decorated = items.map((item) => {
      // Flatten the predicates from hds-nav-data.ts to serializable flags.
      if (label === 'Foundations' && item.path === '/color') return { ...item, exact: true };
      if (label === 'Components' && item.path.startsWith('/components')) {
        return { ...item, indent: true };
      }
      return item;
    });
    sections.push({ label, items: decorated });
  }

  return { $generated: GENERATED_NOTE, sections };
}

function main() {
  const repoRoot = process.env.FIXTURE_DIR || DEFAULT_ROOT;
  const model = buildNavModel(repoRoot);
  const outPath = path.join(repoRoot, 'src', 'app', 'data', 'nav-model.json');
  writeFileSync(outPath, `${JSON.stringify(model, null, 2)}\n`);
  const count = model.sections.reduce((n, s) => n + s.items.length, 0);
  console.log(
    `OK ${path.relative(repoRoot, outPath)} (${model.sections.length} sections, ${count} items)`,
  );
}

// Run main only when invoked directly (not when imported by the drift test).
if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}
