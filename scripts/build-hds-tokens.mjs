#!/usr/bin/env node
/**
 * scripts/build-hds-tokens.mjs
 *
 * Produces two drop-in artifacts for consuming projects:
 *
 *   dist/hds-tokens.js   — ESM module; exports a `tokens` object whose
 *                          values are resolved CSS custom property names
 *                          (e.g. "var(--semantic-color-surface-page)").
 *
 *   dist/hds-tokens.css  — Flat CSS file; all :root custom properties
 *                          fully resolved to their primitive values so
 *                          the file can stand alone without the full app.
 *
 * Usage:
 *   node scripts/build-hds-tokens.mjs   # writes dist/
 *   pnpm tokens:build                   # same via package.json
 *
 * No new npm dependencies required.
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

// Re-use the existing build-tokens utilities so token resolution stays
// in sync with the main tokens.css build.
import {
  walkTokens,
  pathToCSSVar,
  valueToCSS,
  resolveAlias,
  expandTypography,
  expandMotion,
  expandElevation,
  TYPO_PROPS,
  buildTSTree,
  serialize,
} from './build-tokens.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const DIST = join(ROOT, 'dist');

// ── Load token source ───────────────────────────────────────────────────────
const raw = JSON.parse(readFileSync(join(ROOT, 'hirobius.tokens.json'), 'utf8'));
const allTokens = [...walkTokens(raw)];

// ── Build flat CSS (all tiers, fully resolved to primitive values) ──────────
/**
 * Emits a single flat CSS string where every custom property is resolved to
 * its primitive literal value — no var() chains, no aliases. Safe to drop
 * into any project without the rest of the HDS stylesheet.
 */
function buildFlatCss() {
  const lines = [
    '/**',
    ' * Hirobius Design System — hds-tokens.css',
    ' * Generated by scripts/build-hds-tokens.mjs',
    ' * Source: hirobius.tokens.json (W3C DTCG 2025.10)',
    ' *',
    ' * All custom properties are resolved to primitive literal values.',
    ' * Drop this file into any project to consume HDS tokens without',
    ' * the full design-system stylesheet.',
    ' *',
    ' * Dark-mode overrides (semantic-tier tokens) are emitted under',
    ' * [data-theme="dark"] so the same drop-in file handles both modes.',
    ' */',
    '',
    ':root {',
  ];
  const darkLines = [];

  for (const { path, type, value, extensions } of allTokens) {
    const cssVar = pathToCSSVar(path);

    // Fully resolve to primitive for flat output (preserveAlias = false)
    try {
      if (type === 'typography') {
        lines.push(`  /* ${path.join('.')} */`);
        for (const { cssVar: v, cssValue } of expandTypography(path, value)) {
          // Resolve any remaining aliases in composite values
          const resolved = cssValue?.startsWith('var(')
            ? valueToCSS(resolveAlias(
                Object.values(value ?? {}).find(
                  (_, i) => {
                    const [, key] = TYPO_PROPS[i] ?? [];
                    return key != null;
                  }
                ) ?? cssValue,
                type,
                false,
                raw,
              ) ?? cssValue, type, false, raw)
            : cssValue;
          if (resolved) lines.push(`  ${v}: ${resolved};`);
        }
      } else if (type === 'motion') {
        lines.push(`  /* ${path.join('.')} */`);
        for (const { cssVar: v, cssValue } of expandMotion(path, value)) {
          if (cssValue) lines.push(`  ${v}: ${cssValue};`);
        }
      } else if (type === 'elevation') {
        lines.push(`  /* ${path.join('.')} */`);
        for (const { cssVar: v, cssValue } of expandElevation(path, value)) {
          if (cssValue) lines.push(`  ${v}: ${cssValue};`);
        }
      } else {
        // Resolve aliases all the way to their primitive literal
        const resolved = valueToCSS(value, type, /* preserveAlias */ false, raw);
        if (resolved != null) lines.push(`  ${cssVar}: ${resolved};`);

        // Dark-mode overrides
        const dark = extensions?.['com.figma.variables']?.modes?.Dark;
        if (dark) {
          const darkResolved = valueToCSS(resolveAlias(dark, raw), type, false, raw);
          if (darkResolved) darkLines.push(`  ${cssVar}: ${darkResolved};`);
        }
      }
    } catch {
      // Skip tokens that can't be fully resolved (e.g. circular or missing refs)
    }
  }

  lines.push('}', '');

  if (darkLines.length > 0) {
    lines.push('[data-theme="dark"] {', ...darkLines, '}', '');
  }

  return lines.join('\n');
}

// ── Build ESM JS module ─────────────────────────────────────────────────────
/**
 * Emits an ESM module that exports a `tokens` object. Leaves are CSS var()
 * strings — the same runtime values as the main generated-tokens.ts but
 * without the TypeScript machinery, suitable for plain JS consumers.
 */
function buildEsmJs() {
  const tsTree = buildTSTree(allTokens);
  const inner = serialize(tsTree);

  return [
    '/**',
    ' * Hirobius Design System — hds-tokens.js',
    ' * Generated by scripts/build-hds-tokens.mjs',
    ' * Source: hirobius.tokens.json (W3C DTCG 2025.10)',
    ' *',
    ' * ESM module. Import in any JS/TS project:',
    ' *   import { tokens } from "./hds-tokens.js";',
    ' *   tokens.semantic.color.surface.page // "var(--semantic-color-surface-page)"',
    ' *',
    ' * Pair with hds-tokens.css for the custom property definitions.',
    ' */',
    '',
    `export const tokens = {`,
    inner,
    `};`,
    '',
    'export default tokens;',
    '',
  ].join('\n');
}

// ── Write artifacts ─────────────────────────────────────────────────────────
mkdirSync(DIST, { recursive: true });

const cssContent = buildFlatCss();
const jsContent = buildEsmJs();

writeFileSync(join(DIST, 'hds-tokens.css'), cssContent);
writeFileSync(join(DIST, 'hds-tokens.js'), jsContent);

const cssLines = cssContent.split('\n').length;
const jsLines = jsContent.split('\n').length;

console.log(`✔ dist/hds-tokens.css  (${cssLines} lines)`);
console.log(`✔ dist/hds-tokens.js   (${jsLines} lines)`);
